package com.example.myapplication.db.entity;

import androidx.annotation.NonNull;
import androidx.room.ColumnInfo;
import androidx.room.Entity;
import androidx.room.Ignore;


//============================================================
//　処理概要　:　確認明細ワークテーブル（W_KAKUNIN_MEISAI）エンティティ
//　対　　象　:　確認作業中の一時明細データを保持する
//　主キー　　:　HEAT_NO + SOKUBAN（複合キー）
//============================================================
@Entity(
        tableName = "W_KAKUNIN_MEISAI",
        primaryKeys = {"HEAT_NO", "SOKUBAN"}
)
public class KakuninMeisaiWorkEntity {

    //============================================================
    //　項目概要　:　ヒート番号（主キー）
    //　内　　容　:　製品ロットを識別する番号
    //============================================================
    @NonNull
    @ColumnInfo(name = "HEAT_NO")
    public String heatNo;
    // ・複合主キーその1


    //============================================================
    //　項目概要　:　側番（主キー）
    //　内　　容　:　同一ヒート内の識別番号
    //============================================================
    @NonNull
    @ColumnInfo(name = "SOKUBAN")
    public String sokuban;
    // ・複合主キーその2


    //============================================================
    //　項目概要　:　出荷指図番号
    //　内　　容　:　出荷指示に紐づく番号
    //============================================================
    @ColumnInfo(name = "SYUKKA_SASHIZU_NO")
    public String syukkaSashizuNo;
    // ・表示／確認用情報


    //============================================================
    //　項目概要　:　バンドル番号
    //　内　　容　:　束識別番号
    //============================================================
    @ColumnInfo(name = "BUNDLE_NO")
    public String bundleNo;
    // ・コンテナ内での束識別用


    //============================================================
    //　項目概要　:　重量
    //　内　　容　:　明細単位の重量
    //============================================================
    @ColumnInfo(name = "JYURYO")
    public Integer jyuryo;
    // ・単位は業務仕様に依存


    //============================================================
    //　項目概要　:　コンテナID
    //　内　　容　:　紐づく確認コンテナID
    //============================================================
    @ColumnInfo(name = "CONTAINER_ID")
    public String containerId;
    // ・未設定の場合はNULL


    //============================================================
    //　項目概要　:　コンテナ照合確認フラグ
    //　内　　容　:　確認済 여부（Boolean）
    //============================================================
    @ColumnInfo(name = "CONTAINER_SYOUGO_KAKUNIN")
    public Boolean containerSyougoKakunin;
    // ・true  : 確認済
    // ・false : 未確認


    //============================================================
    //　項目概要　:　登録処理名
    //　内　　容　:　登録時の処理名称
    //============================================================
    @ColumnInfo(name = "INSERT_PROC_NAME")
    public String insertProcName;
    // ・登録元機能名などを保持


    //============================================================
    //　項目概要　:　登録日
    //　内　　容　:　登録日（TEXT保存）
    //============================================================
    @ColumnInfo(name = "INSERT_YMD")
    public String insertYmd;
    // ・yyyyMMdd形式などで保存想定


    //============================================================
    //　項目概要　:　更新処理名
    //　内　　容　:　最終更新時の処理名称
    //============================================================
    @ColumnInfo(name = "UPDATE_PROC_NAME")
    public String updateProcName;
    // ・更新元機能名などを保持


    //============================================================
    //　項目概要　:　更新日
    //　内　　容　:　最終更新日（TEXT保存）
    //============================================================
    @ColumnInfo(name = "UPDATE_YMD")
    public String updateYmd;
    // ・yyyyMMdd形式などで保存想定


    //============================================================
    //　項目概要　:　確認コンテナID（DB非保持）
    //　内　　容　:　画面／処理用一時保持値
    //============================================================
    @Ignore
    public Integer kakuninContainerId;
    // ・Roomには保存されない


    //============================================================
    //　項目概要　:　確認ステータス（DB非保持）
    //　内　　容　:　画面制御用ステータス値
    //============================================================
    @Ignore
    public Integer kakuninStatus;
    // ・内部処理用フラグ


    //============================================================
    //　項目概要　:　削除フラグ（DB非保持）
    //　内　　容　:　論理削除管理用フラグ
    //============================================================
    @Ignore
    public Integer deleteFlg;
    // ・画面制御／一時管理用途


    //============================================================
    //　項目概要　:　予約番号（DB非保持）
    //　内　　容　:　JOIN結果保持用項目
    //============================================================
    @Ignore
    public String bookingNo;
    // ・JOINや画面表示用の一時項目

}