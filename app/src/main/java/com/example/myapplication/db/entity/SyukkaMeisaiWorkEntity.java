package com.example.myapplication.db.entity;

import androidx.annotation.NonNull;
import androidx.room.ColumnInfo;
import androidx.room.Entity;
import androidx.room.Ignore;


//============================================================
//　処理概要　:　出荷明細ワークテーブル（W_SYUKKA_MEISAI）エンティティ
//　対　　象　:　出荷作業中の一時明細データを保持する
//　主キー　　:　HEAT_NO + SOKUBAN（複合キー）
//============================================================
@Entity(
        tableName = "W_SYUKKA_MEISAI",
        primaryKeys = {"HEAT_NO", "SOKUBAN"}
)
public class SyukkaMeisaiWorkEntity {

    //============================================================
    //　項目概要　:　鋼番（主キー）
    //　内　　容　:　製品ロットを識別する番号
    //============================================================
    @NonNull
    @ColumnInfo(name = "HEAT_NO")
    public String heatNo;
    // ・複合主キーその1


    //============================================================
    //　項目概要　:　束番（主キー）
    //　内　　容　:　同一鋼番内の識別番号
    //============================================================
    @NonNull
    @ColumnInfo(name = "SOKUBAN")
    public String sokuban;
    // ・複合主キーその2


    //============================================================
    //　項目概要　:　登録処理名
    //　内　　容　:　登録時の処理名称
    //============================================================
    @ColumnInfo(name = "INSERT_PROC_NAME")
    public String insertProcName;
    // ・ワーク投入元機能名などを保持


    //============================================================
    //　項目概要　:　登録年月日
    //　内　　容　:　登録年月日（TEXT保存）
    //============================================================
    @ColumnInfo(name = "INSERT_YMD")
    public String insertYmd;
    // ・yyyy-MM-dd'T'HH:mm:ss


    //============================================================
    //　項目概要　:　コンテナID（DB非保持）
    //　内　　容　:　画面表示／一時保持用項目
    //============================================================
    @Ignore
    public Integer containerId;
    // ・Roomには保存されない
    // ・JOIN結果や処理中の保持用


    //============================================================
    //　項目概要　:　更新日（DB非保持）
    //　内　　容　:　画面表示／一時管理用
    //============================================================
    @Ignore
    public String updateYmd;
    // ・ワークテーブルには存在しない項目
    // ・本体テーブルJOIN時の補助項目などで使用

}