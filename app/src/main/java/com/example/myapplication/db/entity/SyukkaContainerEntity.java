package com.example.myapplication.db.entity;

import androidx.annotation.NonNull;
import androidx.room.ColumnInfo;
import androidx.room.Entity;
import androidx.room.Ignore;


//============================================================
//　処理概要　:　出荷コンテナテーブル（T_SYUKKA_CONTAINER）エンティティ
//　対　　象　:　出荷処理に使用するコンテナ情報を保持する
//　主キー　　:　CONTAINER_ID
//============================================================
@Entity(
        tableName = "T_SYUKKA_CONTAINER",
        primaryKeys = {"CONTAINER_ID"}
)
public class SyukkaContainerEntity {

    //============================================================
    //　項目概要　:　コンテナID（主キー）
    //　内　　容　:　出荷コンテナを一意に識別するID
    //============================================================
    @ColumnInfo(name = "CONTAINER_ID")
    @NonNull
    public Integer containerId;
    // ・採番により管理される想定


    //============================================================
    //　項目概要　:　作業予定日
    //　内　　容　:　出荷作業予定日
    //============================================================
    @ColumnInfo(name = "SAGYOU_YOTEI_YMD")
    public String sagyouYoteiYmd;
    // ・yyyyMMdd等で保存想定


    //============================================================
    //　項目概要　:　コンテナ番号
    //　内　　容　:　表示用コンテナ番号
    //============================================================
    @ColumnInfo(name = "CONTAINER_NO")
    public String containerNo;
    // ・帳票／画面表示用


    //============================================================
    //　項目概要　:　コンテナ重量
    //　内　　容　:　コンテナ自重
    //============================================================
    @ColumnInfo(name = "CONTAINER_JYURYO")
    public Integer containerJyuryo;
    // ・重量計算用基準値


    //============================================================
    //　項目概要　:　ダンネージ重量
    //　内　　容　:　梱包材重量
    //============================================================
    @ColumnInfo(name = "DUNNAGE_JYURYO")
    public Integer dunnageJyuryo;
    // ・重量計算に加算する値


    //============================================================
    //　項目概要　:　シール番号
    //　内　　容　:　コンテナ封印番号
    //============================================================
    @ColumnInfo(name = "SEAL_NO")
    public String sealNo;
    // ・出荷管理用識別情報


    //============================================================
    //　項目概要　:　予約番号（DB非保持）
    //　内　　容　:　JOIN結果や画面表示用一時項目
    //============================================================
    @Ignore
    public String bookingNo;
    // ・Roomには保存されない


    //============================================================
    //　項目概要　:　コンテナサイズ（DB非保持）
    //　内　　容　:　画面表示／計算用サイズ区分
    //============================================================
    @Ignore
    public Integer containerSize;
    // ・内部処理用


    //============================================================
    //　項目概要　:　データ送信日時
    //　内　　容　:　送信完了日時（ISO形式TEXT）
    //============================================================
    @ColumnInfo(name = "DATA_SEND_YMDHMS")
    public String dataSendYmdhms;
    // ・未送信の場合はNULL


    //============================================================
    //　項目概要　:　登録処理名
    //　内　　容　:　登録時の処理名称
    //============================================================
    @ColumnInfo(name = "INSERT_PROC_NAME")
    public String insertProcName;
    // ・登録元機能名などを保持


    //============================================================
    //　項目概要　:　登録日
    //　内　　容　:　登録日（TEXT保存）
    //============================================================
    @ColumnInfo(name = "INSERT_YMD")
    public String insertYmd;
    // ・yyyy-MM-dd等で保存想定


    //============================================================
    //　項目概要　:　更新処理名
    //　内　　容　:　最終更新時の処理名称
    //============================================================
    @ColumnInfo(name = "UPDATE_PROC_NAME")
    public String updateProcName;
    // ・更新元機能名などを保持


    //============================================================
    //　項目概要　:　更新日
    //　内　　容　:　最終更新日（TEXT保存）
    //============================================================
    @ColumnInfo(name = "UPDATE_YMD")
    public String updateYmd;
    // ・yyyy-MM-dd等で保存想定

}