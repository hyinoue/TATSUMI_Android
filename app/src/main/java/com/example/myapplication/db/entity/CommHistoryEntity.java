package com.example.myapplication.db.entity;

import androidx.annotation.NonNull;
import androidx.room.ColumnInfo;
import androidx.room.Entity;


//============================================================
//　処理概要　:　通信履歴テーブル（C_COMM_HISTORY）エンティティ
//　対　　象　:　通信処理の実行履歴を保持する
//　主キー　　:　LOG_ID
//============================================================
@Entity(
        tableName = "C_COMM_HISTORY",
        primaryKeys = {"LOG_ID"}
)
public class CommHistoryEntity {

    //============================================================
    //　項目概要　:　ログID（主キー）
    //　内　　容　:　通信履歴を一意に識別するID
    //============================================================
    @ColumnInfo(name = "LOG_ID")
    @NonNull
    public String logId;
    // ・通信履歴の一意キー
    // ・日付＋連番などで構成される想定


    //============================================================
    //　項目概要　:　処理開始日時
    //　内　　容　:　通信処理開始時刻（yyyyMMddHHmmss想定）
    //============================================================
    @ColumnInfo(name = "START_YMDHMS")
    public String startYmdhms;
    // ・処理開始タイムスタンプ


    //============================================================
    //　項目概要　:　処理終了日時
    //　内　　容　:　通信処理終了時刻
    //============================================================
    @ColumnInfo(name = "END_YMDHMS")
    public String endYmdhms;
    // ・処理完了時刻
    // ・異常終了時は未設定の可能性あり


    //============================================================
    //　項目概要　:　処理名
    //　内　　容　:　実行した処理の名称
    //============================================================
    @ColumnInfo(name = "PROC_NAME")
    public String procName;
    // ・API名やバッチ名などを格納


    //============================================================
    //　項目概要　:　引数情報
    //　内　　容　:　処理実行時の引数内容
    //============================================================
    @ColumnInfo(name = "ARGUMENT")
    public String argument;
    // ・リクエスト内容などを保存


    //============================================================
    //　項目概要　:　戻り値
    //　内　　容　:　処理結果の返却値
    //============================================================
    @ColumnInfo(name = "RETURN_VALUE")
    public String returnValue;
    // ・レスポンス内容や結果コード


    //============================================================
    //　項目概要　:　エラー内容
    //　内　　容　:　異常発生時のエラー詳細
    //============================================================
    @ColumnInfo(name = "ERR_DESCRIPTION")
    public String errDescription;
    // ・例外メッセージやエラー説明を格納

}