//============================================================
//　処理概要　:　settings.gradle.kts（プロジェクト全体設定）
//　対　　象　:　ビルドプラグイン管理／依存リポジトリ管理／モジュール構成
//　内　　容　:　・pluginManagement（Gradleプラグイン解決）
//　　　　　　:　・dependencyResolutionManagement（依存関係解決）
//　　　　　　:　・rootProject名設定
//　　　　　　:　・モジュールinclude設定
//============================================================


//============================================================
//　処理概要　:　プラグイン管理設定
//　内　　容　:　Gradleプラグイン解決時に参照するリポジトリを定義
//============================================================
pluginManagement {
    repositories {

        //============================================================
        //　処理概要　:　Googleリポジトリ
        //　内　　容　:　Android関連プラグインのみ取得対象を制限
        //============================================================
        google {
            content {
                includeGroupByRegex("com\\.android.*")
                includeGroupByRegex("com\\.google.*")
                includeGroupByRegex("androidx.*")
            }
        }

        //============================================================
        //　処理概要　:　Maven Central
        //============================================================
        mavenCentral()

        //============================================================
        //　処理概要　:　Gradle Plugin Portal
        //============================================================
        gradlePluginPortal()
    }
}


//============================================================
//　処理概要　:　依存関係解決管理
//　内　　容　:　プロジェクト全体で使用するリポジトリを定義
//============================================================
dependencyResolutionManagement {

    //============================================================
    //　処理概要　:　プロジェクト側リポジトリ禁止
    //　内　　容　:　各モジュールでrepositoriesを定義するとビルドエラーにする
    //　目　　的　:　リポジトリ定義をsettingsに統一し管理を簡潔化
    //============================================================
    repositoriesMode.set(RepositoriesMode.FAIL_ON_PROJECT_REPOS)

    repositories {

        //============================================================
        //　処理概要　:　Google公式リポジトリ
        //============================================================
        google()

        //============================================================
        //　処理概要　:　Maven Central
        //============================================================
        mavenCentral()

        //============================================================
        //　処理概要　:　JitPack
        //　内　　容　:　GitHub公開ライブラリ取得用
        //============================================================
        maven {
            url = uri("https://jitpack.io")
        }
    }
}


//============================================================
//　処理概要　:　ルートプロジェクト名設定
//============================================================
rootProject.name = "My Application"


//============================================================
//　処理概要　:　モジュール定義
//　内　　容　:　ビルド対象モジュールを指定
//============================================================
include(":app")